package DataType;

public class DataType {

	public static void main(String[] args) {
		
		int a=10;             // Primitive data type = int,short,char,double,------
		double d=22.5;
		char c='H';           // Derived data type = Array,ArrayList,String,HashMap
		boolean b=true;
		String s="welcome";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		System.out.println(s);

	}

}
