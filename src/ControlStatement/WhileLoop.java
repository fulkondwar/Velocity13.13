package ControlStatement;

public class WhileLoop {

	public static void main(String[] args) {
		int k=1;
		while(k<=10)
		{
			System.out.print(" " + k);
			k++;
		}
		// even number 1-10
		int i=2;
		while(i<=10)
		{
			System.out.print(" "+ i);
			i+=2; //i=i+2
		}
		// odd number
		int j=1;
		while(j<=10)
		{
			System.out.print(" "+j);
			j+=2; //j=j+2
		}

	}

}
